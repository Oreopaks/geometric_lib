def square_area(a):
    '''Принимает a -  длинну стороны квадрата и возвращает его площадь
       input: a = 12
       output: 144'''
    return abs(a * a)

def square_perimeter(a):
    '''Принимает a - длинну стороны квадрата и возвращает его площадь
       input: a = 6
       output: 48'''
    return abs(4 * a)
